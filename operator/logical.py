
#logical operators

# and operator

x = 5
y = 10

print(f"The result of x and y is {x and y}")

# or operator

x = 5
y = 0

print(f"The result of x or y is {x or y}")

# not operator

x = 5

print(f"The result of not x is {not x}")

# comparison operators  

x = 5
y = 10

print(f"The result of x == y is {x == y}")
print(f"The result of x != y is {x != y}")

print(f"The result of x > y is {x > y}")

print(f"The result of x < y is {x < y}")